def names(name):        #parameter
    print(f"Hello!, {name}")
    print(f"what's up!, {name}")
    print(f"How do you do, {name}")
names("Mama")           #passing_argument
names("Nobita")
names("Niggs")


#In the above code "Mama","Nobita","Niggs" are arguments(Real values) and "name" is parameter

